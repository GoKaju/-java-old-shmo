import java.sql.Timestamp;

/**
 *
 * @author D4V3
 */
public class ServiciosVO {
    private String  serv_id;
    private String  serv_nombre;
    private String  serv_observacion;
    private String  serv_registradopor;
    private String  serv_estado;
    private Timestamp  serv_fechacambio;

    public String getServ_estado() {
        return serv_estado;
    }

    public void setServ_estado(String serv_estado) {
        this.serv_estado = serv_estado;
    }

    public String getServ_id() {
        return serv_id;
    }

    public void setServ_id(String serv_id) {
        this.serv_id = serv_id;
    }

    public String getServ_nombre() {
        return serv_nombre;
    }

    public void setServ_nombre(String serv_nombre) {
        this.serv_nombre = serv_nombre;
    }

    public String getServ_observacion() {
        return serv_observacion;
    }

    public void setServ_observacion(String serv_observacion) {
        this.serv_observacion = serv_observacion;
    }

    public String getServ_registradopor() {
        return serv_registradopor;
    }

    public void setServ_registradopor(String serv_registradopor) {
        this.serv_registradopor = serv_registradopor;
    }

    public Timestamp getServ_fechacambio() {
        return serv_fechacambio;
    }

    public void setServ_fechacambio(Timestamp serv_fechacambio) {
        this.serv_fechacambio = serv_fechacambio;
    }
    
    
    
}
