package ocupacional.valueobjects;

import java.sql.Timestamp;

/**
 *
 * @author VALERIA
 */
public class EstadoCivilVO {
    private String esci_id;
    private String esci_descripcion;
    private Timestamp esci_fechacambio;
    private String esci_registradopor;

    public String getEsci_id() {
        return esci_id;
    }

    public void setEsci_id(String esci_id) {
        this.esci_id = esci_id;
    }

    public String getEsci_descripcion() {
        return esci_descripcion;
    }

    public void setEsci_descripcion(String esci_descripcion) {
        this.esci_descripcion = esci_descripcion;
    }

    public Timestamp getEsci_fechacambio() {
        return esci_fechacambio;
    }

    public void setEsci_fechacambio(Timestamp esci_fechacambio) {
        this.esci_fechacambio = esci_fechacambio;
    }

    public String getEsci_registradopor() {
        return esci_registradopor;
    }

    public void setEsci_registradopor(String esci_registradopor) {
        this.esci_registradopor = esci_registradopor;
    }
    
    
}
