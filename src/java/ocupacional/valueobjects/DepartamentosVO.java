package ocupacional.valueobjects;

import java.sql.Timestamp;

/**
 *
 * @author VALERIA
 */
public class DepartamentosVO {
    private String depa_id;
    private String pais_id;
    private String depa_nombre;
    private Timestamp depa_fechacambio;
    private String depa_registradopor;

    public String getDepa_id() {
        return depa_id;
    }

    public void setDepa_id(String depa_id) {
        this.depa_id = depa_id;
    }

    public String getPais_id() {
        return pais_id;
    }

    public void setPais_id(String pais_id) {
        this.pais_id = pais_id;
    }

    public String getDepa_nombre() {
        return depa_nombre;
    }

    public void setDepa_nombre(String depa_nombre) {
        this.depa_nombre = depa_nombre;
    }

    public Timestamp getDepa_fechacambio() {
        return depa_fechacambio;
    }

    public void setDepa_fechacambio(Timestamp depa_fechacambio) {
        this.depa_fechacambio = depa_fechacambio;
    }

    public String getDepa_registradopor() {
        return depa_registradopor;
    }

    public void setDepa_registradopor(String depa_registradopor) {
        this.depa_registradopor = depa_registradopor;
    }
    
    
    
    
}
