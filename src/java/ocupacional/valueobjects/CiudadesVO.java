package ocupacional.valueobjects;

import java.sql.Timestamp;

/**
 *
 * @author VALERIA
 */
public class CiudadesVO {
    private String ciud_id;
    private String depa_id;
    private String ciud_nombre;
    private Timestamp ciud_fechacambio;
    private String ciud_registradopor;

    public String getCiud_id() {
        return ciud_id;
    }

    public void setCiud_id(String ciud_id) {
        this.ciud_id = ciud_id;
    }

    public String getDepa_id() {
        return depa_id;
    }

    public void setDepa_id(String depa_id) {
        this.depa_id = depa_id;
    }

    public String getCiud_nombre() {
        return ciud_nombre;
    }

    public void setCiud_nombre(String ciud_nombre) {
        this.ciud_nombre = ciud_nombre;
    }

    public Timestamp getCiud_fechacambio() {
        return ciud_fechacambio;
    }

    public void setCiud_fechacambio(Timestamp ciud_fechacambio) {
        this.ciud_fechacambio = ciud_fechacambio;
    }

    public String getCiud_registradopor() {
        return ciud_registradopor;
    }

    public void setCiud_registradopor(String ciud_registradopor) {
        this.ciud_registradopor = ciud_registradopor;
    }

        
}
